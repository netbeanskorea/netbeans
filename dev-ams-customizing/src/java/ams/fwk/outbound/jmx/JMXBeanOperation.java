package ams.fwk.outbound.jmx;

import java.util.List;

public class JMXBeanOperation implements java.io.Serializable {

	private static final long serialVersionUID = -2284740040163422190L;

	public String name;
	public String returnType;
	public List<JMXBeanOperationParameter> paramters;
	public Object value;
	public String exception;

	public String toString() {
		StringBuilder sb = new StringBuilder();
		sb.append("name=").append(name);
		sb.append(", returnType=").append(returnType);
		sb.append(", paramters=").append(paramters);
		sb.append(", value=").append(value);
		sb.append(", exception=").append(exception);
		return sb.toString();
	}
}
